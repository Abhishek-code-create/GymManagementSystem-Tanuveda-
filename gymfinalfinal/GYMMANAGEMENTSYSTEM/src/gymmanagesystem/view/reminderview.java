    private void setupTable() {
        DefaultTableModel model = new DefaultTableModel(new Object[][]{}, new String[]{
                "Date", "Task", "Status", "Duly on", "Action"
        }) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 4; // Only 'Action' column is editable (for delete button)
            }
        };
        jTable1.setModel(model);
        jTable1.getColumn("Action").setCellRenderer(new ButtonRenderer());
        jTable1.getColumn("Action").setCellEditor(new ButtonEditor(new JButton()));
    }

    // Renderer for Delete button
    class ButtonRenderer extends JButton implements TableCellRenderer {
        public ButtonRenderer() {
            setText("Delete");
        }
        public Component getTableCellRendererComponent(javax.swing.JTable table, Object value,
                                                       boolean isSelected, boolean hasFocus, int row, int column) {
            return this;
        }
    }

    // Editor for Delete button
    class ButtonEditor extends AbstractCellEditor implements TableCellEditor, ActionListener {
        private JButton button;
        private int row;
        public ButtonEditor(JButton btn) {
            this.button = btn;
            button.setText("Delete");
            button.addActionListener(this);
        }
        public Component getTableCellEditorComponent(javax.swing.JTable table, Object value,
                                                     boolean isSelected, int row, int column) {
            this.row = row;
            return button;
        }
        public Object getCellEditorValue() { return "Delete"; }
        public void actionPerformed(ActionEvent e) {
            ((DefaultTableModel) jTable1.getModel()).removeRow(row);
            fireEditingStopped();
        }
    }

