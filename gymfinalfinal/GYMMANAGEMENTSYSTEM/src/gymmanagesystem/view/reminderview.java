    // Dialog for adding/updating reminders
    private void showReminderDialog(int rowToUpdate) {
        JPanel panel = new JPanel(new java.awt.GridLayout(0, 2, 5, 5));
        panel.add(new JLabel("Date:"));
        JSpinner dateSpinner = new JSpinner(new SpinnerDateModel());
        dateSpinner.setEditor(new JSpinner.DateEditor(dateSpinner, "yyyy-MM-dd HH:mm"));
        panel.add(dateSpinner);
        panel.add(new JLabel("Task:"));
        JTextField taskField = new JTextField();
        panel.add(taskField);
        panel.add(new JLabel("Status:"));
        JComboBox<String> statusBox = new JComboBox<>(new String[]{"Ongoing", "Completed"});
        panel.add(statusBox);
        panel.add(new JLabel("Duly on:"));
        JSpinner dulySpinner = new JSpinner(new SpinnerDateModel());
        dulySpinner.setEditor(new JSpinner.DateEditor(dulySpinner, "yyyy-MM-dd HH:mm"));
        panel.add(dulySpinner);

        if (rowToUpdate >= 0) {
            // Pre-fill fields for update
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            dateSpinner.setValue(new Date()); // You may want to parse the string to Date
            taskField.setText((String) model.getValueAt(rowToUpdate, 1));
            statusBox.setSelectedItem(model.getValueAt(rowToUpdate, 2));
            dulySpinner.setValue(new Date()); // You may want to parse the string to Date
        }

        int result = JOptionPane.showConfirmDialog(this, panel, rowToUpdate >= 0 ? "Update Reminder" : "Add Reminder", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            Object[] rowData = new Object[]{
                    ((JSpinner.DateEditor) dateSpinner.getEditor()).getFormat().format(dateSpinner.getValue()),
                    taskField.getText(),
                    statusBox.getSelectedItem(),
                    ((JSpinner.DateEditor) dulySpinner.getEditor()).getFormat().format(dulySpinner.getValue()),
                    "Delete"
            };
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            if (rowToUpdate >= 0) {
                // Update
                for (int i = 0; i < 4; i++) model.setValueAt(rowData[i], rowToUpdate, i);
            } else {
                // Add
                model.addRow(rowData);
            }
        }
    }

